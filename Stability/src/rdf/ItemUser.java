package rdf;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement
@XmlType (propOrder={"user","probPositive","probNegative","impactPositive","impactNegative"})
public class ItemUser {

	private String user;
	private double probPositive;
	private double probNegative;
	private double impactPositive;
	private double impactNegative;
	
	
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public double getProbPositive() {
		return probPositive;
	}
	public void setProbPositive(double probPositive) {
		this.probPositive = probPositive;
	}
	public double getProbNegative() {
		return probNegative;
	}
	public void setProbNegative(double probNegative) {
		this.probNegative = probNegative;
	}
	public double getImpactPositive() {
		return impactPositive;
	}
	public void setImpactPositive(double impactPositive) {
		this.impactPositive = impactPositive;
	}
	public double getImpactNegative() {
		return impactNegative;
	}
	public void setImpactNegative(double impactNegative) {
		this.impactNegative = impactNegative;
	}
	
	
	
	
}
